                <div class="container">
                    <header class="section-header">
                        <h3>About Us</h3>
                        <p class='wow fadeInUp about'>Energreen evolved from being a power engineering firm designing high voltage power systems such as power substations, transmission lines, and metering systems for the Philippines major electric utilities like National Power Corp., National Electrification Administration, PNOC, and Meralco. The group executed over 160 time-sensitive projects worth over $250 million over a 20 year period with a 99.8% on time completion and collection record. </p>
                        <p class='wow fadeInUp about'>After 20 years it decided in 1999 to remake itself into an independent power producer specializing in the niche market for small bite-sized power projects of 1 to 50 mw for the isolated islands where it felt it has an overall competitive advantage.</p>
                        <p class='wow fadeInUp about'>Energreen’s core team is composed of professionals who have been working with rural electric cooperatives for decades with a unique understanding of the needs and dynamics of these non-profit distribution utilities.</p>
                        <p class='wow fadeInUp about'>While most of the independent power producers were concentrating on larger power projects in the main grid of Luzon and the Visayas, Energreen’s team saw the potential of the largely neglected needs of the electric cooperatives in the small islands. It pioneered the development of bankable power supply agreements that are tailor made for the unique requirements of the electric coops and its isolated island grid. </p>
                        <p class='wow fadeInUp about'>Energreen’s early work was rewarded by electricity supply agreements for the larger islands of Palawan, Oriental Mindoro, and Catanduanes. The projects were originally developed under development company affiliate Power One Corp. and taken over by Energreen for implementation under an equity streamlining agreement.</p>
